# Defines variable containing tab.
tabby_cat = "\tI'm tabbed in."
# Defines variable containing line break (LF).
persian_cat = "I'm split\non a line."
# Defines variable containing backslashes.
backslash_cat = "I'm \\ a \\ cat."

# Defines variable as multi-line string containing tabs
# and 1 gratuitous line break.
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''

# Prints variables defined above.
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
